import UIKit

extension UIColor {
    static let groceryPrimaryColor: UIColor = { return UIColor(named: "groceryPrimaryColor")! }()
    static let grocerySecondaryBackground: UIColor = { return UIColor(named: "grocerySecondaryColor")! }()
    static let groceryCherryColor: UIColor = { return UIColor(named: "groceryCherryColor")! }()

    static let groceryBlackText: UIColor = { return UIColor(named: "groceryBlackTextColor")! }()
    static let groceryGreyText: UIColor = { return UIColor(named: "groceryGreyTextColor")! }()
    static let groceryWhiteText: UIColor = { return UIColor(named: "groceryWhiteTextColor")! }()
}
