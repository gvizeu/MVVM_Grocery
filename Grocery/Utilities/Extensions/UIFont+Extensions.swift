import UIKit

extension UIFont {
    class func arial(size: CGFloat) -> UIFont? {
        return UIFont(name: "Arial", size: size)
    }

    class func arialBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Arial Bold", size: size)
    }
}
